#Contexto: El portero de un baile quiere saber si una persona puede entrar o no, dependiendo de su edad. Si la persona tiene 18 años o más, puede entrar. Si es menor de 18 años, no puede entrar.

edad_usuario = int(input("¿Cuántos años tenés? "))
if edad_usuario >= 18:
    print("Puedes pasar")
else:
    print("No podés pasar, sos menor de edad")










